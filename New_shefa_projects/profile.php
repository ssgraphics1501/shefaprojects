<h3 class="boldBlue">Company Profile</h3>

<p style="text-align:justify"><strong>SHEFA </strong>is engaged in  Designing / Supply / <strong>Turnkey Projects</strong> / Erection &amp; Commissioning  of  Equipments of  SS304, SS316 and SS904  &amp; in special alloy such as Inconel, Monel &amp;<a name="_GoBack" id="_GoBack"></a>  Hastalloy construction with a vision of  providing value added, well engineered solutions in the field of <strong>Chemical </strong><strong>Process  Plants with Centralized Control Automation Solutions</strong>. <br />
  <br />
  <strong>In  our long journey of 3 decades</strong>,  we have achieved various milestones and have proclaimed  to be the trendsetters in Pharmaceutical,  Chemical, Petrochemical, Bulk Drug, Oil, Paint and Ink Machinery <strong>( Explosive Zone )</strong> certified in  compliance to Industrial codes such as TEMA, ASME, API Chief Controller of  explosive and GMP standard. </p>
  
<p style="text-align:justify">We have designed a number of  process plants with all Automation Packages with a view to <strong>Increase  Average Efficiency, Economy, Better Quality, Minimum Labour dependency and  Omission of Human Errors </strong>in production process &amp; incorporating present Plant  &amp; Utilities with minimum modification.</p>


